#!/usr/bin/env node
const fs = require('fs');
const path = require('path');

const projectRoot = path.resolve(__dirname, '..');
const venturesDir = path.join(projectRoot, 'assets', 'images', 'ventures');
const outputFile = path.join(projectRoot, 'js', 'venture-image-manifest.js');
const allowedExtensions = new Set(['.jpg', '.jpeg', '.png', '.webp', '.avif']);
const collator = new Intl.Collator(undefined, { numeric: true, sensitivity: 'base' });

function listImages(folderPath, ventureId) {
  if (!fs.existsSync(folderPath)) return [];

  return fs.readdirSync(folderPath, { withFileTypes: true })
    .filter((entry) => entry.isFile())
    .map((entry) => entry.name)
    .filter((filename) => allowedExtensions.has(path.extname(filename).toLowerCase()))
    .sort((a, b) => collator.compare(a, b))
    .map((filename) => `assets/images/ventures/${ventureId}/${filename}`);
}

function generateManifest() {
  const manifest = {};

  if (fs.existsSync(venturesDir)) {
    fs.readdirSync(venturesDir, { withFileTypes: true })
      .filter((entry) => entry.isDirectory())
      .map((entry) => entry.name)
      .sort((a, b) => collator.compare(a, b))
      .forEach((ventureId) => {
        manifest[ventureId] = listImages(path.join(venturesDir, ventureId), ventureId);
      });
  }

  const output = [
    '/* This file is generated by scripts/generate-venture-image-manifest.js. */',
    '/* Add images under assets/images/ventures/<venture-id>/, then rerun the generator. */',
    `window.VENTURE_IMAGE_MANIFEST = ${JSON.stringify(manifest, null, 2)};`,
    ''
  ].join('\n');

  fs.writeFileSync(outputFile, output);
  console.log(`Generated ${path.relative(projectRoot, outputFile)}`);
  Object.entries(manifest).forEach(([ventureId, images]) => {
    console.log(`${ventureId}: ${images.length} image(s)`);
  });
}

generateManifest();
